<h2>第5条（個人情報の第三者提供）</h2>
<p>当サイトは、個人情報の第三者（取引の相手方も含む）への提供について、次のとおり取り扱います。</p>

<p><strong>1. 教材取引に伴う相手方への提供</strong></p>
<p>本サービスにおける教材の販売・購入その他の取引を履行・管理するため、当サイトは、取引に必要な範囲で、取引相手方の氏名、配送先住所、連絡先電話番号その他当該取引の遂行に必要な情報を相手方に提供することがあります。ユーザーは、本サービスを利用して取引を行うことにより、かかる情報提供に同意したものとみなされます。</p>
<p>販売者は、前項に基づき取得した個人情報を、当該取引の遂行および関係法令に基づく記録保管の目的に限り利用するものとし、これらの目的を超えて利用してはならないものとします。</p>

<p><strong>2. 著作権侵害等に関する提供</strong></p>
<p>著作権その他の知的財産権の侵害、詐欺その他の違法又は不正な行為が疑われる場合において、権利者その他の関係者から適切な申立てがあり、当サイトが当該申立ての内容を確認したうえで必要と認めるときは、当サイトは、必要最小限の範囲で、販売者等の個人情報（氏名、住所、連絡先等）その他の関係情報を当該権利者等に開示することがあります。</p>

<p><strong>3. 法令等に基づく第三者への提供</strong></p>
<p>当サイトは、前各項の場合のほか、次に掲げる場合には、あらかじめユーザーの同意を得ることなく、個人情報を第三者に提供することがあります。ただし、個人情報保護法その他の法令で認められる場合に限ります。</p>
<ul>
  <li>人の生命、身体または財産の保護のために必要がある場合であって、本人の同意を得ることが困難であるとき</li>
  <li>公衆衛生の向上または児童の健全な育成の推進のために特に必要がある場合であって、本人の同意を得ることが困難であるとき</li>
  <li>国の機関もしくは地方公共団体またはその委託を受けた者が法令の定める事務を遂行することに対して協力する必要がある場合であって、本人の同意を得ることにより当該事務の遂行に支障を及ぼすおそれがあるとき</li>
  <li>予め次の事項を告知あるいは公表し、かつ当サイトが個人情報保護委員会に届出をしたとき
    <ul>
      <li>利用目的に第三者への提供を含むこと</li>
      <li>第三者に提供されるデータの項目</li>
      <li>第三者への提供の手段または方法</li>
      <li>本人の求めに応じて個人情報の第三者への提供を停止すること</li>
      <li>本人の求めを受け付ける方法</li>
    </ul>
  </li>
</ul>

<p><strong>4. 第三者に該当しない場合</strong></p>
<p>前項の定めにかかわらず、次に掲げる場合には、当該情報の提供先は第三者に該当しないものとします。</p>
<ul>
  <li>当サイトが利用目的の達成に必要な範囲内において個人情報の取扱いの全部または一部を委託する場合</li>
  <li>合併その他の事由による事業の承継に伴って個人情報が提供される場合</li>
  <li>個人情報を特定の者との間で共同して利用する場合であって、その旨並びに共同して利用される個人情報の項目、共同して利用する者の範囲、利用する者の利用目的および当該個人情報の管理について責任を有する者の氏名または名称について、あらかじめ本人に通知し、または本人が容易に知り得る状態に置いた場合</li>
</ul>
<p>当サイトは、前項第1号に基づき個人情報の取扱いを委託する場合、個人情報を適正に取扱う能力を有すると認められる者を選定するとともに、当該委託先に対して適切な契約上の義務付け及び監督を行います。</p>
